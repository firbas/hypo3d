	SUBROUTINE SETFN(TN,IN,FN)
	CHARACTER TN(30),FN*30,TNP*30
	DATA TNP/'            '/
	WRITE(TNP,903)(TN(I),I=1,IN)
	WRITE(*,904)TNP
  904   FORMAT(1X,A30,'  Set file name : ')
	READ(*,901)FN
  901	FORMAT(A30)
	IF(FN.EQ.'        ')WRITE(FN,903)(TN(I),I=1,IN)
  903   FORMAT(30A1)
	WRITE(*,902)FN
  902	FORMAT(1X,A30)
	write(*,*)
	RETURN
	END
